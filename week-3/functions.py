#functions are block of code that are executed together 
#they use the def keyword
def print_name():
    print("My name is Nathaniel")
    print("I am very very tired")
    print("My dream is to always be happy")
    print("I am eternally grateful to my parents")
    print("God is surely alive")
def add_numbers(num1 , num2):
    sum_num = num1 + num2
    print(sum_num)
#calling/making the function
print_name()
print("                                        ")
add_numbers(21,69) #passing the parameters
add_numbers(23,56)
add_numbers(99,23)

def diff_numbers(num1 , num2):
    diff_numbers = num1 - num2
    print(diff_numbers)
print("                                        ")
diff_numbers(69,52)
diff_numbers(99,67)
diff_numbers(56,23)
